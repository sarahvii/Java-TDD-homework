public class Calculator {

    public Calculator(){}

    public int addTotal(int val1, int val2){
        return val1 + val2;
    }

    public int subtractTotal(int val1, int val2){
        return val1 - val2;
    }

    public int multiplyTotal(int val1, int val2){
        return val1 * val2;
    }

    public double divideTotal(double val1, double val2){
        return val1 / val2;
    }




}
